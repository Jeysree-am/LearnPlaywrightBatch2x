//Assignment operator = 
let a = 10; //a is assigned the value of 10
console.log(a); //10    
a=40;
console.log(a); //40
//Arithmetic operators +, -, *, /, %
let x = 10;
let y = 20;
console.log(x+y); //30
console.log(x-y); //-10
console.log(x*y);   //200
console.log(x/y); //0.5
console.log(x%y); //10