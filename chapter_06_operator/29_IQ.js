//Compound operators
let a = 10;
a += 5; // equivalent to a = a + 5
console.log(a); //15
a -= 3; // equivalent to a = a - 3
console.log(a); //12
a *= 2; // equivalent to a = a * 2
console.log(a); //24
a /= 4; // equivalent to a = a / 4
console.log(a); //6
a %= 4; // equivalent to a = a % 4
console.log(a); //2