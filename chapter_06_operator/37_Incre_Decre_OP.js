//pre  incremment and post increment
let x=10;
console.log(++x); //pre increment: x is incremented first and then printed, Output: 11  
let y=10;
console.log(y++); //post increment: y is printed first and then incremented, Output: 10
console.log(y); //Output: 11

//Pre Decrement and Post Decrement
let a=10;
console.log(--a); //pre decrement: a is decremented first and then printed, Output: 9
let b=10;
console.log(b--); //post decrement: b is printed first and then decremented, Output: 10
console.log(b); //Output: 9
